import json
import unittest
import unittest.mock

from go_space import board_lib, types


class BoardTest(unittest.TestCase):
    @unittest.mock.patch("go_space.consts.SIZE", 3)
    def test_sgf_format(self):
        board = board_lib.Board()
        board.place(types.Point.fromLabel("aa"), types.Player.Black)
        board.place(types.Point.fromLabel("ba"), types.Player.White)
        board.place(types.Point.fromLabel("ac"), types.Player.Black)
        self.assertEqual(board.ascii_board(), ("#O.\n" "...\n" "#.."))

    @unittest.mock.patch("go_space.consts.SIZE", 3)
    def test_a1_format(self):
        board = board_lib.Board()
        board.place(types.Point.fromLabel("A3"), types.Player.Black)
        board.place(types.Point.fromLabel("B3"), types.Player.White)
        board.place(types.Point.fromLabel("A1"), types.Player.Black)
        self.assertEqual(board.ascii_board(), ("#O.\n" "...\n" "#.."))

    @unittest.mock.patch("go_space.consts.SIZE", 3)
    def test_json_read(self):
        json_str = '{"black": ["aa", "ac"], "white": ["ba"]}'
        brd = json.loads(json_str)
        self.assertEqual(
            board_lib.boardFromBwBoardStr(brd).ascii_board(), ("#O.\n" "...\n" "#..")
        )

    def test_remove_stones(self):
        board = board_lib.Board()
        board.place(point=types.Point(row=1, col=1), player=types.Player.White)
        board.place(point=types.Point(row=0, col=1), player=types.Player.Black)
        board.place(point=types.Point(row=2, col=1), player=types.Player.Black)
        board.place(point=types.Point(row=1, col=0), player=types.Player.Black)
        board.place(point=types.Point(row=1, col=2), player=types.Player.Black)
        self.assertEqual(
            board.ascii_board(),
            """.#.................
               #.#................
               .#.................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................""".replace(
                " ", ""
            ),
        )

    def test_remove_stones_corner(self):
        board = board_lib.Board()
        board.place(point=types.Point(row=0, col=0), player=types.Player.White)
        board.place(point=types.Point(row=1, col=0), player=types.Player.Black)
        board.place(point=types.Point(row=0, col=1), player=types.Player.Black)
        self.assertEqual(
            board.ascii_board(),
            """.#.................
               #..................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................""".replace(
                " ", ""
            ),
        )

    def test_remove_stones_blob(self):
        board = board_lib.Board()
        board.place(point=types.Point(row=1, col=1), player=types.Player.White)
        board.place(point=types.Point(row=1, col=2), player=types.Player.White)
        board.place(point=types.Point(row=2, col=2), player=types.Player.White)
        board.place(point=types.Point(row=0, col=1), player=types.Player.Black)
        board.place(point=types.Point(row=0, col=2), player=types.Player.Black)
        board.place(point=types.Point(row=0, col=3), player=types.Player.Black)
        board.place(point=types.Point(row=1, col=0), player=types.Player.Black)
        board.place(point=types.Point(row=1, col=3), player=types.Player.Black)
        board.place(point=types.Point(row=2, col=1), player=types.Player.Black)
        board.place(point=types.Point(row=2, col=3), player=types.Player.Black)
        board.place(point=types.Point(row=3, col=2), player=types.Player.Black)
        self.assertEqual(
            board.ascii_board(),
            """.###...............
               #..#...............
               .#.#...............
               ..#................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................
               ...................""".replace(
                " ", ""
            ),
        )
